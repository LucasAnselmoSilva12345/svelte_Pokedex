<h1 class="text-4xl text-center my-8 uppercase">About this site</h1>

<p class="mb-4">
	Pokémon é a abreviação de Poketto Monsuta ou Pocket Monster, algo como Monstro de Bolso. São
	chamados assim porque as inúmeras espécies de Pokémons podem ser abrigadas dentro de Pokébolas, um
	dispositivo de um treinador Pokémon ou até de um professor, de uma enfermeira, ou de outra pessoa
	que se envolva com essas criaturas. Dentro desses pequenos itens, um Pokémon, por maior que seja,
	pode viajar no bolso de um treinador.
</p>

<p>
	Atualmente 809 Pokémons são conhecidos, considerando que provavelmente muitos já foram extintos à
	milhares de anos. Um Pokémon pode ser classificado por tipo, denominado principalmente pelos
	ataques e pelo habitat da criatura. Cada Pokémon pode ser incluído em até dois tipos dos dezessete
	atuais: aquático, dragão, elétrico, fantasma, fogo, gelo, grama, inseto, lutador, metálico,
	normal, noturno, psíquico, rocha, terrestre, venenoso e voador. Em uma batalha Pokémon, cada tipo
	possui sua fraqueza e vantagem. Por exemplo: um Pokémon do tipo água é forte contra um Pokémon do
	tipo fogo, rocha ou terrestre, porém é fraco contra um do tipo elétrico ou grama.
</p>
